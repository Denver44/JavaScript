// Promisifying setTimeout
const wait = function (seconds) {
  // it is not mandatory to pass both args in promise executor function
  return new Promise(function (resolve) {
    // It is not mandatory that a resolve function must have an argument
    setTimeout(resolve, seconds * 1000);
  });
};

wait(1)
  .then(() => {
    console.log('1 second passed');
    return wait(1);
  })
  .then(() => {
    console.log('2 second passed');
    return wait(1);
  })
  .then(() => {
    console.log('3 second passed');
    return wait(1);
  })
  .then(() => console.log('4 second passed'));

// We can resolve a promise instantly
Promise.resolve('abc').then((x) => console.log(x));

// We can reject a promise instantly also
Promise.reject(new Error('Problem!')).catch((x) => console.error(x));


